public class Logical {
    public static void main(String[] args) {
        // and operator
        System.out.println((5>2)&&(4<2));
        System.out.println((5>2)&&(4<2));
        // || operator it means OR
        System.out.println((5>2)||(4<2));
        System.out.println((5>2)||(4<2));
        System.out.println((5>2)||(4<2));
        //! Operator
        System.out.println(!(5==3));  //false
        System.out.println(!(5>3));  //true
    }
}
