public class assign {
    public static void main(String[] args) {
        int a=100;
        int var;
        var=a;
        System.out.println("var using=" +var);
        var += a;
        System.out.println("var using +=: " + var);
        // assign value using =*
        var *= a;
        System.out.println("var using *=: " + var);

    }
}
